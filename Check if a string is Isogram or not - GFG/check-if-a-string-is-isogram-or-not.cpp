class Solution
{
    public:
    //Function to check if a string is Isogram or not.
    bool isIsogram(string s)
    {
        vector<int> freq(26,0);
        for(auto&x : s)
        {
            freq[x-'a']++;
            if(freq[x-'a']>1) return false;
        }
        return true;
    }
};

